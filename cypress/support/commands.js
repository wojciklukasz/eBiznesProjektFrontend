Cypress.Commands.add('login', () => {
    localStorage.setItem('email', 'test@test.com')
    localStorage.setItem('token', 'test')
    expect(localStorage.getItem('email')).eq('test@test.com')
    expect(localStorage.getItem('token')).eq('test')
})